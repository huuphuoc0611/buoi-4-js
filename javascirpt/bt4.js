let a = prompt("nhập số a: "),
    b = prompt("nhập số b: "),
    phep = prompt("nhập toán tử: ");
switch(phep){
    case "+":
        console.log(a + b);
        break;
      case "-":
        console.log(a - b);
        break;
      case "*":
        console.log(a * b);
        break;
      case "/":
        console.log(a / b);
        break;
} 